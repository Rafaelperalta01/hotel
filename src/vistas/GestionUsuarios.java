    private void cargarTabla(Usuarios users) {
        modelo.addRow(new Object[]{
            users.getNombre(),
            users.getApellido(),
            users.getDni(),
            users.getSexo(),
            users.getDireccion(),
            users.getCargo(),
            users.isEstado(),
            users.getContrase単a(),
            users.getEmail()
        });
    }

    private void llenarTabla() {
        UsuariosData ud = new UsuariosData();
        for (Usuarios user : ud.listarUsuarios()) {
            cargarTabla(user);
        }
    }

